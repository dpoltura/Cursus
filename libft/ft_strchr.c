#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
		i++;
	while (s[j] != c)
	{
		j++;
		if (s[j] == '\0' && s[j] != c)
			return (NULL);
	}
	return ((char *)s + j);
}
