int	ft_atoi(char *str)
{
	int	i;
	int	j;
	int	nbr;

	i = 0;
	j = 1;
	nbr = 0;
	while (str [i] >= 9 && str [i] <= 13 || str [i] == ' ')
		i++;
	while (str [i] == '-' || str [i] == '+')
	{
		if (str [i] == '-')
			j = j * -1;
		i++;
	}
	while (str [i] >= '0' && str [i] <= '9')
	{
		nbr = nbr * 10 + (str[i] - '0');
		i++;
		if (str [i] < '0' || str [i] > '9')
			return (nbr * j);
	}
	return (0);
}
