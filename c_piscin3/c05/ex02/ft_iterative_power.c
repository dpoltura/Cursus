int	ft_iterative_power(int nb, int power)
{
	int	res;
	int	tmp;

	res = 0;
	tmp = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power == 1)
		return (nb);
	while (power > 1)
	{
		res = tmp * nb;
		tmp = res;
		power--;
	}
	return (res);
}
