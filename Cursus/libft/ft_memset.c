#include <unistd.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	int				*cpy;

	i = 0;
	cpy = s;
	while (i < n)
	{
		cpy[i] = c;
		i++;
	}
	return (s);
}
