#include <unistd.h>

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while (s[i] != c)
	{
		i--;
		if (i < 0)
			return (NULL);
	}
	return ((char *)s + i);
}
